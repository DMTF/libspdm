/**
 * Test 4: SPDM 1.2 multi-request transcript with VCA.
 * Format: VCA + GET_MEASUREMENTS(0)/MEASUREMENTS(0) + GET_MEASUREMENTS(1)/MEASUREMENTS(1)
 *       + GET_MEASUREMENTS(2)/MEASUREMENTS(2) [signed]
 **/
static void req_parse_measurement_transaction_data_case4(void **state)
{
    libspdm_return_t status;
    spdm_measurement_block_common_header_t *measurements;
    size_t measurement_count;
    uint8_t record_buf[256];
    uint32_t record_size;

    test_buf_reset();

    /* Build VCA */
    test_build_vca(SPDM_MESSAGE_VERSION_12);

    /* GET_MEASUREMENTS(0): total number query, no signature */
    test_build_get_measurements_request(
        SPDM_MESSAGE_VERSION_12, 0, 0);

    /* MEASUREMENTS(0): total number = 2, no blocks */
    test_build_measurements_response(
        SPDM_MESSAGE_VERSION_12, 2, 0, NULL, 0, false);

    /* GET_MEASUREMENTS(1): index 1, no signature */
    test_build_get_measurements_request(
        SPDM_MESSAGE_VERSION_12, 0, 1);

    /* MEASUREMENTS(1): 1 block with index 1 */
    test_build_measurement_record(1, 1, record_buf, sizeof(record_buf), &record_size);
    test_build_measurements_response(
        SPDM_MESSAGE_VERSION_12, 0, 1, record_buf, record_size, false);

    /* GET_MEASUREMENTS(2): index 2, with signature (last request) */
    test_build_get_measurements_request(
        SPDM_MESSAGE_VERSION_12,
        SPDM_GET_MEASUREMENTS_REQUEST_ATTRIBUTES_GENERATE_SIGNATURE,
        2);

    /* MEASUREMENTS(2): 1 block with index 2 */
    test_build_measurement_record(2, 1, record_buf, sizeof(record_buf), &record_size);
    test_build_measurements_response(
        SPDM_MESSAGE_VERSION_12, 0, 1, record_buf, record_size, true);

    status = libspdm_parse_measurement_transaction_data(
        m_test_buffer, m_test_offset, &measurements, &measurement_count);
    assert_int_equal(status, LIBSPDM_STATUS_SUCCESS);

    /* Should have 2 blocks total (from responses 1 and 2) */
    assert_int_equal(measurement_count, 2);
    assert_non_null(measurements);

    /* Validate individual blocks */
    {
        const uint8_t *ptr = (const uint8_t *)measurements;
        const spdm_measurement_block_common_header_t *block;

        block = (const spdm_measurement_block_common_header_t *)ptr;
        assert_int_equal(block->index, 1);
        ptr += sizeof(spdm_measurement_block_common_header_t) + block->measurement_size;

        block = (const spdm_measurement_block_common_header_t *)ptr;
        assert_int_equal(block->index, 2);
    }

    free(measurements);
}


/*
 * Test helpers for building SPDM transaction data buffers.
 */
#define TEST_MEAS_VALUE_SIZE 8
#define TEST_FAKE_SIGNATURE_SIZE 64
#define TEST_MAX_BUFFER_SIZE 4096

static uint8_t m_test_buffer[TEST_MAX_BUFFER_SIZE];
static size_t m_test_offset;

static void test_buf_reset(void)
{
    memset(m_test_buffer, 0, sizeof(m_test_buffer));
    m_test_offset = 0;
}

static void test_buf_write_uint8(uint8_t val)
{
    assert(m_test_offset + 1 <= TEST_MAX_BUFFER_SIZE);
    m_test_buffer[m_test_offset++] = val;
}

static void test_buf_write_uint16_le(uint16_t val)
{
    assert(m_test_offset + 2 <= TEST_MAX_BUFFER_SIZE);
    m_test_buffer[m_test_offset++] = (uint8_t)(val & 0xFF);
    m_test_buffer[m_test_offset++] = (uint8_t)((val >> 8) & 0xFF);
}

static void test_buf_write_uint24_le(uint32_t val)
{
    assert(m_test_offset + 3 <= TEST_MAX_BUFFER_SIZE);
    m_test_buffer[m_test_offset++] = (uint8_t)(val & 0xFF);
    m_test_buffer[m_test_offset++] = (uint8_t)((val >> 8) & 0xFF);
    m_test_buffer[m_test_offset++] = (uint8_t)((val >> 16) & 0xFF);
}

static void test_buf_write_uint32_le(uint32_t val)
{
    assert(m_test_offset + 4 <= TEST_MAX_BUFFER_SIZE);
    m_test_buffer[m_test_offset++] = (uint8_t)(val & 0xFF);
    m_test_buffer[m_test_offset++] = (uint8_t)((val >> 8) & 0xFF);
    m_test_buffer[m_test_offset++] = (uint8_t)((val >> 16) & 0xFF);
    m_test_buffer[m_test_offset++] = (uint8_t)((val >> 24) & 0xFF);
}

static void test_buf_write_zeros(size_t count)
{
    assert(m_test_offset + count <= TEST_MAX_BUFFER_SIZE);
    memset(m_test_buffer + m_test_offset, 0, count);
    m_test_offset += count;
}

static void test_buf_write_bytes(const uint8_t *data, size_t size)
{
    assert(m_test_offset + size <= TEST_MAX_BUFFER_SIZE);
    memcpy(m_test_buffer + m_test_offset, data, size);
    m_test_offset += size;
}

static void test_buf_write_header(uint8_t version, uint8_t code, uint8_t param1, uint8_t param2)
{
    test_buf_write_uint8(version);
    test_buf_write_uint8(code);
    test_buf_write_uint8(param1);
    test_buf_write_uint8(param2);
}

/*
 * Build a single DMTF measurement block with a given index and fill value.
 * Returns the total block size written.
 */
static size_t test_build_measurement_block(uint8_t index, uint8_t fill_value)
{
    size_t start = m_test_offset;
    uint16_t meas_size = sizeof(spdm_measurement_block_dmtf_header_t) + TEST_MEAS_VALUE_SIZE;

    /* Common header */
    test_buf_write_uint8(index);
    test_buf_write_uint8(SPDM_MEASUREMENT_SPECIFICATION_DMTF);
    test_buf_write_uint16_le(meas_size);

    /* DMTF header */
    test_buf_write_uint8(SPDM_MEASUREMENT_BLOCK_MEASUREMENT_TYPE_IMMUTABLE_ROM);
    test_buf_write_uint16_le(TEST_MEAS_VALUE_SIZE);

    /* Measurement value */
    memset(m_test_buffer + m_test_offset, fill_value, TEST_MEAS_VALUE_SIZE);
    m_test_offset += TEST_MEAS_VALUE_SIZE;

    return m_test_offset - start;
}

/*
 * Build VCA for SPDM 1.2+. Constructs minimal valid VCA data.
 */
static void test_build_vca(uint8_t spdm_version)
{
    uint16_t neg_alg_length;
    uint16_t alg_length;

    /* GET_VERSION: header only (version=0x10, code=0x84) */
    test_buf_write_header(SPDM_MESSAGE_VERSION_10, SPDM_GET_VERSION, 0, 0);

    /* VERSION: header + reserved + version_count + version entries */
    test_buf_write_header(SPDM_MESSAGE_VERSION_10, SPDM_VERSION, 0, 0);
    test_buf_write_uint8(0); /* reserved */
    test_buf_write_uint8(2); /* version_number_entry_count */
    test_buf_write_uint16_le(0x0010); /* version 1.0 */
    test_buf_write_uint16_le(((uint16_t)spdm_version << 8) | spdm_version);

    /* GET_CAPABILITIES (1.2+): 20 bytes */
    test_buf_write_header(spdm_version, SPDM_GET_CAPABILITIES, 0, 0);
    test_buf_write_uint8(0); /* reserved */
    test_buf_write_uint8(0); /* ct_exponent */
    test_buf_write_uint16_le(0); /* ext_flags (reserved pre-1.4) */
    test_buf_write_uint32_le(0); /* flags */
    test_buf_write_uint32_le(0x100); /* data_transfer_size */
    test_buf_write_uint32_le(0x1200); /* max_spdm_msg_size */

    /* CAPABILITIES (1.2+): 20 bytes (no supported_algorithms for simplicity) */
    test_buf_write_header(spdm_version, SPDM_CAPABILITIES, 0, 0);
    test_buf_write_uint8(0); /* reserved */
    test_buf_write_uint8(0); /* ct_exponent */
    test_buf_write_uint16_le(0); /* ext_flags */
    test_buf_write_uint32_le(
        SPDM_GET_CAPABILITIES_RESPONSE_FLAGS_MEAS_CAP_SIG); /* flags */
    test_buf_write_uint32_le(0x100); /* data_transfer_size */
    test_buf_write_uint32_le(0x1200); /* max_spdm_msg_size */

    /*
     * NEGOTIATE_ALGORITHMS: 32 bytes
     * header(4) + length(2) + measurement_spec(1) + other_params(1)
     * + base_asym_algo(4) + base_hash_algo(4) + pqc_asym_algo(4)
     * + reserved2(8) + ext_asym_count(1) + ext_hash_count(1)
     * + reserved3(1) + mel_spec(1) = 32
     */
    neg_alg_length = 32;
    test_buf_write_header(spdm_version, SPDM_NEGOTIATE_ALGORITHMS, 0, 0);
    test_buf_write_uint16_le(neg_alg_length);
    test_buf_write_uint8(SPDM_MEASUREMENT_SPECIFICATION_DMTF); /* measurement_spec */
    test_buf_write_uint8(0); /* other_params_support */
    test_buf_write_uint32_le(
        SPDM_ALGORITHMS_BASE_ASYM_ALGO_TPM_ALG_ECDSA_ECC_NIST_P256); /* base_asym_algo */
    test_buf_write_uint32_le(
        SPDM_ALGORITHMS_BASE_HASH_ALGO_TPM_ALG_SHA_256); /* base_hash_algo */
    test_buf_write_uint32_le(0); /* pqc_asym_algo (reserved in pre-1.4) */
    test_buf_write_zeros(8); /* reserved2 */
    test_buf_write_uint8(0); /* ext_asym_count */
    test_buf_write_uint8(0); /* ext_hash_count */
    test_buf_write_uint8(0); /* reserved3 */
    test_buf_write_uint8(0); /* mel_specification */

    /*
     * ALGORITHMS: 36 bytes
     * header(4) + length(2) + measurement_spec_sel(1) + other_params_sel(1)
     * + measurement_hash_algo(4) + base_asym_sel(4) + base_hash_sel(4)
     * + pqc_asym_sel(4) + reserved2(7) + mel_spec_sel(1)
     * + ext_asym_sel_count(1) + ext_hash_sel_count(1) + reserved3(2) = 36
     */
    alg_length = 36;
    test_buf_write_header(spdm_version, SPDM_ALGORITHMS, 0, 0);
    test_buf_write_uint16_le(alg_length);
    test_buf_write_uint8(SPDM_MEASUREMENT_SPECIFICATION_DMTF); /* measurement_spec_sel */
    test_buf_write_uint8(0); /* other_params_selection */
    test_buf_write_uint32_le(
        SPDM_ALGORITHMS_MEASUREMENT_HASH_ALGO_TPM_ALG_SHA_256); /* measurement_hash_algo */
    test_buf_write_uint32_le(
        SPDM_ALGORITHMS_BASE_ASYM_ALGO_TPM_ALG_ECDSA_ECC_NIST_P256); /* base_asym_sel */
    test_buf_write_uint32_le(
        SPDM_ALGORITHMS_BASE_HASH_ALGO_TPM_ALG_SHA_256); /* base_hash_sel */
    test_buf_write_uint32_le(0); /* pqc_asym_sel */
    test_buf_write_zeros(7); /* reserved2 */
    test_buf_write_uint8(0); /* mel_specification_sel */
    test_buf_write_uint8(0); /* ext_asym_sel_count */
    test_buf_write_uint8(0); /* ext_hash_sel_count */
    test_buf_write_uint16_le(0); /* reserved3 */
}

/*
 * Build a GET_MEASUREMENTS request.
 */
static void test_build_get_measurements_request(
    uint8_t spdm_version, uint8_t attributes, uint8_t measurement_operation)
{
    bool signature_requested;

    signature_requested = (attributes &
                           SPDM_GET_MEASUREMENTS_REQUEST_ATTRIBUTES_GENERATE_SIGNATURE) != 0;

    test_buf_write_header(spdm_version, SPDM_GET_MEASUREMENTS, attributes, measurement_operation);

    if (signature_requested) {
        test_buf_write_zeros(SPDM_NONCE_SIZE); /* nonce */
        if (spdm_version >= SPDM_MESSAGE_VERSION_11) {
            test_buf_write_uint8(0); /* slot_id_param */
        }
    }
    if (spdm_version >= SPDM_MESSAGE_VERSION_13) {
        test_buf_write_zeros(SPDM_REQ_CONTEXT_SIZE); /* requester_context */
    }
}

/*
 * Build a MEASUREMENTS response.
 * If record_data and record_size are non-zero, the measurement record is included.
 * nonce is filled with zeros. opaque_length is 0.
 */
static void test_build_measurements_response(
    uint8_t spdm_version, uint8_t param1, uint8_t number_of_blocks,
    const uint8_t *record_data, uint32_t record_size,
    bool include_signature)
{
    test_buf_write_header(spdm_version, SPDM_MEASUREMENTS, param1, 0);
    test_buf_write_uint8(number_of_blocks);
    test_buf_write_uint24_le(record_size);

    if (record_size > 0 && record_data != NULL) {
        test_buf_write_bytes(record_data, record_size);
    }

    /* Nonce (always present) */
    test_buf_write_zeros(SPDM_NONCE_SIZE);

    /* Opaque length = 0 */
    test_buf_write_uint16_le(0);

    /* Requester context (v1.3+) */
    if (spdm_version >= SPDM_MESSAGE_VERSION_13) {
        test_buf_write_zeros(SPDM_REQ_CONTEXT_SIZE);
    }

    /* Fake signature */
    if (include_signature) {
        test_buf_write_zeros(TEST_FAKE_SIGNATURE_SIZE);
    }
}

/*
 * Helper: Build measurement record with N blocks, returning record data and size.
 * Uses a temporary area at the end of the test buffer.
 */
static void test_build_measurement_record(
    uint8_t start_index, uint8_t count,
    uint8_t *record_buf, size_t record_buf_size, uint32_t *record_size_out)
{
    size_t saved_offset;
    size_t block_size;
    uint8_t i;

    saved_offset = m_test_offset;

    for (i = 0; i < count; i++) {
        block_size = test_build_measurement_block(start_index + i, 0xAA + i);
        (void)block_size;
    }

    *record_size_out = (uint32_t)(m_test_offset - saved_offset);
    assert(*record_size_out <= record_buf_size);
    memcpy(record_buf, m_test_buffer + saved_offset, *record_size_out);
    m_test_offset = saved_offset;
}

/*
 * Validate the parsed measurement blocks.
 */
static void test_validate_blocks(
    const spdm_measurement_block_common_header_t *measurements,
    size_t measurement_count,
    uint8_t expected_start_index, size_t expected_count)
{
    const uint8_t *ptr;
    size_t i;
    const spdm_measurement_block_common_header_t *block;
    size_t block_size;

    assert_non_null(measurements);
    assert_int_equal(measurement_count, expected_count);

    ptr = (const uint8_t *)measurements;
    for (i = 0; i < measurement_count; i++) {
        block = (const spdm_measurement_block_common_header_t *)ptr;
        assert_int_equal(block->index, expected_start_index + (uint8_t)i);
        assert_int_equal(block->measurement_specification, SPDM_MEASUREMENT_SPECIFICATION_DMTF);
        block_size = sizeof(spdm_measurement_block_common_header_t) + block->measurement_size;
        ptr += block_size;
    }
}

/**
 * Test 1: SPDM 1.0 single-request transcript with 2 measurement blocks and signature.
 **/
static void req_parse_measurement_transaction_data_case1(void **state)
{
    libspdm_return_t status;
    spdm_measurement_block_common_header_t *measurements;
    size_t measurement_count;
    uint8_t record_buf[256];
    uint32_t record_size;

    test_buf_reset();

    /* Build measurement record with 2 blocks */
    test_build_measurement_record(1, 2, record_buf, sizeof(record_buf), &record_size);

    /* GET_MEASUREMENTS (0xFF, with signature) */
    test_build_get_measurements_request(
        SPDM_MESSAGE_VERSION_10,
        SPDM_GET_MEASUREMENTS_REQUEST_ATTRIBUTES_GENERATE_SIGNATURE,
        0xFF);

    /* MEASUREMENTS response */
    test_build_measurements_response(
        SPDM_MESSAGE_VERSION_10, 0, 2, record_buf, record_size, true);

    status = libspdm_parse_measurement_transaction_data(
        m_test_buffer, m_test_offset, &measurements, &measurement_count);
    assert_int_equal(status, LIBSPDM_STATUS_SUCCESS);

    test_validate_blocks(measurements, measurement_count, 1, 2);

    free(measurements);
}


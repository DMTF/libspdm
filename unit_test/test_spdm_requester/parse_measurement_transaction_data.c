    test_build_measurement_record(1, 1, record_buf, sizeof(record_buf), &record_size);
    test_build_measurements_response(SPDM_MESSAGE_VERSION_13, 0, 1, record_buf, record_size, false);

    /* GET_MEASUREMENTS(2): index 2, no signature */
